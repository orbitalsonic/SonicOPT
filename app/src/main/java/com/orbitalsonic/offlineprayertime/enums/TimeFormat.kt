package com.orbitalsonic.offlineprayertime.enums

/**
 * Represents the format for displaying time.
 */
enum class TimeFormat {

    /**
     * HOUR_12: Displays time in a 12-hour format with AM/PM (e.g., 5:30 AM).
     */
    HOUR_12,

    /**
     * HOUR_24: Displays time in a 24-hour format (e.g., 17:30).
     */
    HOUR_24
}
